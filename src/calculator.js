function add(value1, value2) {
  return value1 + value2;
}

function divide(value, divisionValue) {
  return value / divisionValue;
}

function multiply(value, multipleValue) {
  return value * multipleValue;
}

function subtract(value1, value2) {
  return value1 - value2;
}


export {
  add,
  divide,
  multiply,
  subtract,
};
